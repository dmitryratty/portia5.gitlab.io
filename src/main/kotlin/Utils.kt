
import java.nio.file.Path

object Utils {
    val hostName = "https://dmitryratty.gitlab.io"

    val currentPath: Path = Path.of(System.getProperty("user.dir")).normalize().toRealPath()

    val projectDir: Path = if (currentPath.endsWith("ratty-public"))
        currentPath else currentPath.resolve("ratty-public")

    val resourcesDir: Path = projectDir.resolve("src/main/resources")

    val testResourcesDir: Path = projectDir.resolve("src/test/resources")
    val testAutogeneratedDir: Path = projectDir.resolve("src/test/resources/autogenerated")

    val srcPagesDir: Path = resourcesDir.resolve("pages")
    val srcPagesGeneratedDir = srcPagesDir.resolve("generated")

    val srcOtherDir: Path = resourcesDir.resolve("other")

    val dstDir: Path = projectDir.resolve("public")

    fun splitToParagraphs(text: String): MutableList<String> {
        return text.split("\n\n").toMutableList()
    }

    fun splitParagraphToLines(paragraph: String): List<String> {
        return paragraph.split('\n')
    }

    fun isHyperlink(word: String): Boolean {
        // if (word.contains(" ")) throw IllegalStateException()
        return word.startsWith("http://") || word.startsWith("https://")
    }

    fun isPrime(n: Long): Boolean {
        // Corner case.
        if (n <= 1) {
            return false
        }
        // Check from 2 to n - 1.
        for (i in 2 until n) {
            if (n % i == 0L) {
                return false
            }
        }
        return true
    }
}